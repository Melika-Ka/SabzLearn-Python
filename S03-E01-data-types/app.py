# numeric
x = 5
print(type(x))
y = x + 0.1
print(type(y))
z = 3 + 2j
print(type(z))
# dictionary
dic = {
    "s": 5,
    "melika": "student"
}
print(type(dic))
# boolean
fls = False
print(type(fls))
# set
arr = {1, 2, 3, 4, 5, 4}
print(arr)  # delete duplicated value
print(type(arr))
# sequence type
# ذخیره شدن پشت سر هم
name = "melika karimi"
print(type(name))
print(name[2:4])
top = (1, 2, 3, 4, 5)
print(top[2:4])
print(type(top))

lst = [1, 3, 4, 5]
print(lst[2:4])
print((type(lst)))

# special
none = None
type(print(none))
