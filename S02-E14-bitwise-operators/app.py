#
print("binary of 11 : ", bin(11))

# &
y = 5
print("binary of y : ", bin(y))

z = 7
print("binary of z : ", bin(z))
print("y & z : ", y & z)

# |
y = 5
print("binary of y : ", bin(y))

z = 6
print("binary of z : ", bin(z))
print("y | z : ", y | z)

# ^
y = 5
print("binary of", y, ": ", bin(y))

z = 7
print("binary of ", z, " : ", bin(z))
print("y ^ z : ", y ^ z)

# >>
y = 12
print("binary of", y, ": ", bin(y))

print(y, ">>", 1, ": ", y >> 1)

# <<
y = 12
print("binary of", y, ": ", bin(y))

print(y, "<<", 1, ": ", y << 1)

# ~
y = 5
print("binary of", y, ": ", bin(y))

print("~", y, ": ", ~y)
print("binary of", ~y, ": ", bin(~y))

